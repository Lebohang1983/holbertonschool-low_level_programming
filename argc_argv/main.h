#ifndef MAIN_H
#define MAIN_H

/**
 * main - Prints its name followed by a new line
 * Return: Always 0 (success).
 */
int main(int argc __attribute__((unused)), char *argv[]);

/**
 * main - Entry point, Also print number of arguments starting a 0
 * @argc: count
 * @argv: arguments string
 * Return: INT count of arguments
 */
int main(int argc, char *argv[] __attribute__((unused)));


/**
 * main - prints the arguments plus the application name
 * @argc: count
 * @argv: string with the arguments
 * Return: Always 0.
 */
int main(int argc, char *argv[]);

/**
 * main - multiplies two numbers
 * @argc: counts the arguments.
 * @argv: string array for the arguments entered
 * Return: 0 (Sucess) else 1 (Failure).
 */
int main(int argc, char *argv[]);


/**
 * main - add positive numbers.
 * @argc: count arguments passed in argv
 * @argv: String that holds the passed Arguments.
 * Return: 0 (Sucess) else 1 (Failure).
 */
int main(int argc, char *argv[]);

#endif
